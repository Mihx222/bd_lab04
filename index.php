<p>
– Groaznic de prost! a răspuns prinţesa. Toată noaptea n-am închis ochii. Dumnezeu ştie ce a fost în pat! Am şezut pe ceva tare şi acuma-s toată numai vânătăi! Groaznic!</p>

<p>
Şi atunci au văzut cu toţii că era o prinţesă adevărată, dacă a simţit ea o boabă de mazăre prin douăzeci de saltele şi douăzeci de perne de puf. Aşa de gingaşă la piele nu putea să fie decât numai o prinţesă!</p>

<p>
Şi prinţul a luat-o de nevastă, fiindcă acum ştia hotărât că asta-i prinţesă adevărată; iar boaba de mazăre au pus-o în odaia unde erau bijuteriile coroanei şi se mai poate vedea acolo şi astăzi dacă n-o fi luat-o careva.</p>

<p>
  Au decis ei să facă cea mai frumoasă nuntă din întregul regat, o nuntă despre care vor auzi chiar și cei mai bogați împărați. Au chemat cei mai buni bucătari din lume pentru a găti bucate care merită să stea pe masa prințului și prințesei. Și desigur, era nevoie de cele mai frumoase haine care au fost vreodată confecționate. Prințul s-a adresat la cea mai prestigioasă cusătorie din lume și a dat comandă la o rochie, pentru nevasta sa, și la un costum pentru sine. Și a avut loc cea mai frumoasă nuntă care s-a petrcut vreodată pe întregul pământ.
</p>

